// Run the SQL browser against a throwaway local ClickHouse — no Docker.
//
// Boots an already-installed `clickhouse` binary as a standalone server in a
// gitignored data dir and serves the built SPA *same-origin* from its
// user_files (the same static-handler trick the production deploy uses), with
// basic-auth and no IdP. The browser POSTs queries to "/", which the server's
// built-in handler answers — so the schema tree, results, charts, and the
// schema lineage graph all work offline.
//
//   npm run local      # build + boot, then open http://localhost:18123/sql
//
// Sign in as user "default" with an empty password. The data dir persists across
// runs (your tables survive); delete it to reset. Ctrl-C stops the server.
//
// Env overrides:
//   LOCAL_CH_DIR   data dir (default: .local-ch/ in the repo)
//   CH_HTTP_PORT   HTTP port (default: 18123 — not 8123, to dodge a system CH)
//   CH_TCP_PORT    native port (default: 19000)

import { spawn, spawnSync } from 'node:child_process';
import { mkdirSync, writeFileSync, copyFileSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const dir = resolve(process.env.LOCAL_CH_DIR || resolve(repoRoot, '.local-ch'));
const httpPort = process.env.CH_HTTP_PORT || '18123';
const tcpPort = process.env.CH_TCP_PORT || '19000';
const spa = resolve(repoRoot, 'dist', 'sql.html');

// --- preconditions ---------------------------------------------------------
if (!existsSync(spa)) {
  console.error('✗ dist/sql.html not found — run `npm run build` first (or use `npm run local`).');
  process.exit(1);
}
const ch = findClickhouse();
if (!ch) {
  console.error('✗ No `clickhouse` binary on PATH. Install it, then retry:\n'
    + '    curl https://clickhouse.com/ | sh      # or: brew install clickhouse\n'
    + '    https://clickhouse.com/docs/install');
  process.exit(1);
}

// --- scaffold (config + SPA rewritten each run; the DB store persists) ------
const store = resolve(dir, 'store');
const userFiles = resolve(store, 'user_files');
mkdirSync(userFiles, { recursive: true });
writeFileSync(resolve(dir, 'config.xml'), configXml());
writeFileSync(resolve(dir, 'users.xml'), usersXml());
copyFileSync(spa, resolve(userFiles, 'sql.html'));
writeFileSync(resolve(userFiles, 'sql-config.json'), JSON.stringify({ basic_login: true, idps: [] }));

// --- boot ------------------------------------------------------------------
const url = `http://localhost:${httpPort}/sql`;
process.stdout.write(
  `\n  Altinity SQL Browser — local ClickHouse (${ch.cmd})\n`
  + `  ▸ open    ${url}\n`
  + `  ▸ sign in as user "default" with an empty password\n`
  + `  ▸ data    ${dir}  (gitignored; delete to reset)\n`
  + `  ▸ Ctrl-C to stop\n\n`,
);
const child = spawn(ch.cmd, [...ch.serverArgs, `--config-file=${resolve(dir, 'config.xml')}`], { stdio: 'inherit' });
const stop = () => child.kill('SIGTERM');
process.on('SIGINT', stop);
process.on('SIGTERM', stop);
child.on('exit', (code) => process.exit(code || 0));

// --- helpers ---------------------------------------------------------------
function findClickhouse() {
  // `clickhouse server …` (the multi-tool) or a `clickhouse-server` symlink.
  for (const [cmd, serverArgs] of [['clickhouse', ['server']], ['clickhouse-server', []]]) {
    if (!spawnSync(cmd, ['--version'], { stdio: 'ignore' }).error) return { cmd, serverArgs };
  }
  return null;
}

function usersXml() {
  return `<?xml version="1.0"?>
<clickhouse>
    <profiles><default/></profiles>
    <users>
        <default>
            <password></password>
            <networks><ip>::/0</ip></networks>
            <profile>default</profile>
            <quota>default</quota>
        </default>
    </users>
    <quotas><default/></quotas>
</clickhouse>
`;
}

function configXml() {
  return `<?xml version="1.0"?>
<!-- Generated by build/local.mjs — a throwaway standalone ClickHouse that serves
     the SQL browser same-origin from user_files. Safe to delete with the data dir. -->
<clickhouse>
    <logger>
        <level>warning</level>
        <log>${resolve(dir, 'clickhouse.log')}</log>
        <errorlog>${resolve(dir, 'clickhouse.err.log')}</errorlog>
    </logger>
    <path>${store}/</path>
    <tmp_path>${store}/tmp/</tmp_path>
    <user_files_path>${userFiles}/</user_files_path>
    <user_scripts_path>${store}/user_scripts/</user_scripts_path>
    <users_config>users.xml</users_config>
    <default_profile>default</default_profile>
    <default_database>default</default_database>

    <listen_host>127.0.0.1</listen_host>
    <http_port>${httpPort}</http_port>
    <tcp_port>${tcpPort}</tcp_port>
    <mark_cache_size>268435456</mark_cache_size>

    <!-- ClickHouse 26.x null-pointer-crashes at startup if remote_servers is
         missing/empty (Context::setClustersConfig); one concrete cluster suffices. -->
    <remote_servers>
        <default><shard><replica><host>127.0.0.1</host><port>${tcpPort}</port></replica></shard></default>
    </remote_servers>

    <!-- Serve the SPA + its config from user_files; queries POST to "/" (defaults). -->
    <http_handlers>
        <rule name="sql_browser_spa">
            <url>regex:^/sql/?$</url>
            <methods>GET</methods>
            <handler>
                <type>static</type>
                <content_type>text/html; charset=UTF-8</content_type>
                <http_response_headers>
                    <Cache-Control>no-store</Cache-Control>
                    <Content-Security-Policy>default-src 'none'; script-src 'unsafe-inline'; style-src 'unsafe-inline'; img-src data:; font-src 'self'; frame-src 'self'; connect-src 'self'; base-uri 'none'; frame-ancestors 'none'</Content-Security-Policy>
                    <X-Content-Type-Options>nosniff</X-Content-Type-Options>
                </http_response_headers>
                <response_content>file://sql.html</response_content>
            </handler>
        </rule>
        <rule name="sql_browser_config">
            <url>regex:^/sql/config\\.json$</url>
            <methods>GET</methods>
            <handler>
                <type>static</type>
                <content_type>application/json; charset=UTF-8</content_type>
                <http_response_headers><Cache-Control>no-store</Cache-Control></http_response_headers>
                <response_content>file://sql-config.json</response_content>
            </handler>
        </rule>
        <defaults/>
    </http_handlers>
</clickhouse>
`;
}
