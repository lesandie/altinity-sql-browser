// Deterministically compile every canonical JSON Schema into self-contained
// browser ESM and generate the offline distribution artifacts. Ajv and
// ajv-formats remain build-time-only dependencies.

import Ajv2020 from 'ajv/dist/2020.js';
import addFormats from 'ajv-formats';
import standaloneCode from 'ajv/dist/standalone/index.js';
import { build } from 'esbuild';
import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, relative, resolve } from 'node:path';
import { ANNOTATION_KEYWORDS, SCHEMA_MANIFEST } from './schema-manifest.mjs';
import { buildLibraryBundle, buildSchemaCatalog } from './bundle-json-schemas.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const root = resolve(here, '..');
const generatedSourceDir = resolve(root, 'src/generated');
const generatedSchemaDir = resolve(root, 'schemas/generated');
const validatorsPath = resolve(generatedSourceDir, 'json-schema-validators.js');
const schemasPath = resolve(generatedSourceDir, 'json-schemas.js');
const bundlePath = resolve(generatedSchemaDir, 'library-v2.bundle.schema.json');
const catalogPath = resolve(generatedSchemaDir, 'schema-catalog.json');
const HEADER = '// Generated by build/compile-json-schemas.mjs. Do not edit.\n';

const jsonSource = (value) => JSON.stringify(value, null, 2) + '\n';

async function loadRecords(manifest = SCHEMA_MANIFEST) {
  const records = [];
  const ids = new Map();
  for (const entry of manifest) {
    const path = resolve(root, entry.path);
    const schema = JSON.parse(await readFile(path, 'utf8'));
    if (!schema.$id || typeof schema.$id !== 'string') throw new Error(`${entry.path} has no string $id`);
    if (!schema['x-altinity-kind'] || !Number.isInteger(schema['x-altinity-version'])) {
      throw new Error(`${entry.path} has no canonical kind/version header`);
    }
    if (ids.has(schema.$id)) throw new Error(`Duplicate schema $id in ${ids.get(schema.$id)} and ${entry.path}: ${schema.$id}`);
    ids.set(schema.$id, entry.path);
    records.push({ ...entry, relativePath: entry.path, path, schema });
  }
  return records;
}

async function schemaFilesBelow(dir) {
  let entries;
  try { entries = await readdir(dir, { withFileTypes: true }); } catch (error) {
    if (error.code === 'ENOENT') return [];
    throw error;
  }
  const paths = [];
  for (const entry of entries.sort((a, b) => a.name.localeCompare(b.name))) {
    const path = resolve(dir, entry.name);
    if (entry.isDirectory()) paths.push(...await schemaFilesBelow(path));
    else if (entry.name.endsWith('.schema.json')) paths.push(path);
  }
  return paths;
}

export function assertDraftIdentities(records, drafts) {
  const canonical = new Set(records.map(({ schema }) => schema.$id));
  const seen = new Map();
  for (const { schema, name } of drafts) {
    if (canonical.has(schema.$id)) throw new Error(`Experimental schema reuses canonical $id: ${name}`);
    if (typeof schema.$id !== 'string' || !schema.$id.includes('/drafts/')) {
      throw new Error(`Experimental schema must use a /drafts/ $id: ${name}`);
    }
    if (seen.has(schema.$id)) throw new Error(`Duplicate experimental schema $id in ${seen.get(schema.$id)} and ${name}`);
    seen.set(schema.$id, name);
  }
}

async function validateDraftIdentities(records) {
  const drafts = [];
  for (const path of await schemaFilesBelow(resolve(root, 'docs/drafts'))) {
    drafts.push({ schema: JSON.parse(await readFile(path, 'utf8')), name: relative(root, path) });
  }
  assertDraftIdentities(records, drafts);
}

function createAjv(records) {
  const ajv = new Ajv2020({
    allErrors: true,
    strict: true,
    strictSchema: true,
    validateSchema: true,
    code: { esm: true, source: true, optimize: true },
  });
  addFormats(ajv, { formats: ['date-time'] });
  for (const keyword of ANNOTATION_KEYWORDS) ajv.addKeyword(keyword);
  for (const { schema } of records) ajv.addSchema(schema);
  return ajv;
}

async function validatorSource(records) {
  const ajv = createAjv(records);
  const exports = Object.fromEntries(records.map(({ validatorExport, schema }) => [validatorExport, schema.$id]));
  const standalone = standaloneCode(ajv, exports);
  const bundled = await build({
    stdin: { contents: standalone, resolveDir: root, sourcefile: 'json-schema-standalone.js', loader: 'js' },
    bundle: true,
    format: 'esm',
    platform: 'browser',
    target: 'es2020',
    write: false,
    legalComments: 'none',
    minify: false,
  });
  const registry = records
    .map(({ validatorExport, schema }) => `  ${JSON.stringify(schema.$id)}: ${validatorExport},`)
    .join('\n');
  return `${HEADER}${bundled.outputFiles[0].text}\nexport const validatorsById = {\n${registry}\n};\n`;
}

function schemasModuleSource(records) {
  const declarations = records.map(({ schemaExport, schema }) =>
    `export const ${schemaExport} = ${JSON.stringify(schema, null, 2)};`).join('\n\n');
  const registry = records.map(({ schemaExport, schema }) => `  ${JSON.stringify(schema.$id)}: ${schemaExport},`).join('\n');
  return `${HEADER}${declarations}\n\nexport const schemasById = {\n${registry}\n};\n`;
}

export async function generatedSources({ manifest = SCHEMA_MANIFEST } = {}) {
  const records = await loadRecords(manifest);
  await validateDraftIdentities(records);
  // Constructing and resolving every named validator catches invalid schemas,
  // unresolved refs, and duplicate registrations before any artifact is written.
  const validators = await validatorSource(records);
  return {
    [validatorsPath]: validators,
    [schemasPath]: schemasModuleSource(records),
    [bundlePath]: jsonSource(buildLibraryBundle(records)),
    [catalogPath]: jsonSource(buildSchemaCatalog(records)),
  };
}

export async function checkOrWrite(check, options) {
  const sources = await generatedSources(options);
  await mkdir(generatedSourceDir, { recursive: true });
  await mkdir(generatedSchemaDir, { recursive: true });
  let stale = false;
  for (const [path, source] of Object.entries(sources)) {
    if (check) {
      let current = '';
      try { current = await readFile(path, 'utf8'); } catch { /* missing is stale */ }
      if (current !== source) {
        console.error('stale generated schema artifact: ' + relative(root, path));
        stale = true;
      }
    } else {
      await writeFile(path, source);
      console.log('generated ' + relative(root, path));
    }
  }
  if (stale) process.exitCode = 1;
  return !stale;
}

const isMain = process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (isMain) checkOrWrite(process.argv.includes('--check')).catch((error) => {
  console.error(error);
  process.exit(1);
});

export { loadRecords, validateDraftIdentities };
